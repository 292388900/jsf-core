package com.ipd.jsf.worker.domain;

import java.io.Serializable;
import java.util.Date;

public class SynZkDBLog implements Serializable {
	private static final long serialVersionUID = 1L;

    private int id;

    private String interfaceName;

    private String nodeLog;

	private String type;

	private String creator;

	private Date createTime;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the interfaceName
     */
    public String getInterfaceName() {
        return interfaceName;
    }

    /**
     * @param interfaceName the interfaceName to set
     */
    public void setInterfaceName(String interfaceName) {
        this.interfaceName = interfaceName;
    }

    /**
     * @return the nodeLog
     */
    public String getNodeLog() {
        return nodeLog;
    }

    /**
     * @param nodeLog the nodeLog to set
     */
    public void setNodeLog(String nodeLog) {
        this.nodeLog = nodeLog;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the creator
     */
    public String getCreator() {
        return creator;
    }

    /**
     * @param creator the creator to set
     */
    public void setCreator(String creator) {
        this.creator = creator;
    }

    /**
     * @return the createTime
     */
    public Date getCreateTime() {
        return createTime;
    }

    /**
     * @param createTime the createTime to set
     */
    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

}
